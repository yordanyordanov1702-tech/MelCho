-- Production Load (units/day per line per week per shift)
CREATE TABLE IF NOT EXISTS production_load (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  line_id INTEGER NOT NULL,
  week TEXT NOT NULL,
  shift TEXT NOT NULL DEFAULT 'A',
  planned INTEGER NOT NULL DEFAULT 0,
  capacity INTEGER NOT NULL DEFAULT 0,
  FOREIGN KEY (line_id) REFERENCES lines(id),
  UNIQUE(line_id, week, shift)
);

-- OEE per line per week per shift
CREATE TABLE IF NOT EXISTS oee (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  line_id INTEGER NOT NULL,
  week TEXT NOT NULL,
  shift TEXT NOT NULL DEFAULT 'A',
  oee_percent REAL NOT NULL DEFAULT 0,
  active_machines INTEGER NOT NULL DEFAULT 0,
  FOREIGN KEY (line_id) REFERENCES lines(id),
  UNIQUE(line_id, week, shift)
);

-- Headcount & Absence per shift
CREATE TABLE IF NOT EXISTS headcount (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  line_id INTEGER NOT NULL,
  week TEXT NOT NULL,
  shift TEXT NOT NULL DEFAULT 'A',
  total INTEGER NOT NULL DEFAULT 0,
  absent INTEGER NOT NULL DEFAULT 0,
  FOREIGN KEY (line_id) REFERENCES lines(id),
  UNIQUE(line_id, week, shift)
);

-- Operators & Certifications
CREATE TABLE IF NOT EXISTS operators (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  name TEXT NOT NULL,
  line_id INTEGER NOT NULL,
  cert_expiry TEXT,
  FOREIGN KEY (line_id) REFERENCES lines(id)
);

